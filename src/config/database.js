const mongoose = require('mongoose');

const connectDB = async () => {
  // Skip DB connection if no MongoDB URI is provided
  if (!process.env.MONGODB_URI) {
    console.log('âš ï¸  MongoDB URI not found - running without database');
    return;
  }

  try {
    const conn = await mongoose.connect(process.env.MONGODB_URI);

    console.log(`âœ… MongoDB Connected: ${conn.connection.host}`);
    console.log(`ðŸ“Š Database Name: ${conn.connection.name}`);
  } catch (error) {
    console.error(`âŒ MongoDB Connection Error: ${error.message}`);
    console.log('âš ï¸  Server will continue without database connection');
    // Don't exit process - allow server to run without DB
  }
};

// MongoDB connection event listeners
mongoose.connection.on('connected', () => {
  console.log('ðŸ”— Mongoose connected to MongoDB');
});

mongoose.connection.on('error', (err) => {
  console.error(`ðŸš¨ Mongoose connection error: ${err}`);
});

mongoose.connection.on('disconnected', () => {
  console.log('ðŸ”Œ Mongoose disconnected from MongoDB');
});

// Graceful shutdown
process.on('SIGINT', async () => {
  await mongoose.connection.close();
  console.log('ðŸ›‘ MongoDB connection closed due to app termination');
  process.exit(0);
});

module.exports = connectDB;
